create table categories
(
    id        bigserial
        primary key,
    name      varchar(100) not null,
    parent_id bigint
                           references categories
                               on delete set null,
    constraint uq_category_parent_name
        unique (parent_id, name)
);

alter table categories
    owner to admin;

