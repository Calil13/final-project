create table orders
(
    id            bigserial
        primary key,
    order_date    timestamp   default CURRENT_TIMESTAMP,
    order_status  varchar(20) default 'PENDING'::character varying
        constraint orders_order_status_check
            check ((order_status)::text = ANY
                   ((ARRAY ['CREATED'::character varying, 'PENDING'::character varying, 'PAID'::character varying, 'DELIVERED'::character varying, 'RETURNED'::character varying, 'CANCELLED'::character varying])::text[])),
    customer_id   bigint
        constraint fksjfs85qf6vmcurlx43cnc16gy
            references users,
    day           integer,
    total_amount  numeric(38, 2),
    product_id    bigint
        constraint fkkp5k52qtiygd8jkag4hayd0qg
            references products
            on delete set null,
    delivery_type varchar(255)
        constraint orders_delivery_type_check
            check ((delivery_type)::text = ANY
        ((ARRAY ['DELIVERY'::character varying, 'PICKUP'::character varying])::text[]))
    );

alter table orders
    owner to admin;

create unique index unique_created_order_per_customer
    on orders (customer_id)
    where ((order_status)::text = 'CREATED'::text);

